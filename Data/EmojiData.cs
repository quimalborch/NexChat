        private static List<string> _recentEmojis = new List<string>();
        private const int MaxRecentEmojis = 30;

        public static List<string> GetRecentEmojis()
        {
            return _recentEmojis.ToList();
        }

        public static void AddRecentEmoji(string emoji)
        {
            // Remover si ya existe
            _recentEmojis.Remove(emoji);
            
            // Agregar al inicio
            _recentEmojis.Insert(0, emoji);
            
            // Mantener solo los últimos MaxRecentEmojis
            if (_recentEmojis.Count > MaxRecentEmojis)
            {
                _recentEmojis = _recentEmojis.Take(MaxRecentEmojis).ToList();
            }
        }
